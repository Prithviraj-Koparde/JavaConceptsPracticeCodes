import java.util.Scanner;
public class RelationalOperators {
    public static void main(String[] args) {
        int a= 50 , q= 60;

        System.out.println(a==q);
        System.out.println(a<q);
        System.out.println(a>q);
        System.out.println(a<=q);
        System.out.println(a>=q);
        System.out.println(a!=q);

    }
}
